package com.Kukdoadmin.model;

public class RegPk {

	private int pk;
	private int apk;
	private String name;
	private String email;

	public int getPk() {
		return pk;
	}

	public void setPk(int pk) {
		this.pk = pk;
	}

	public int getApk() {
		return apk;
	}

	public void setApk(int apk) {
		this.apk = apk;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
}
